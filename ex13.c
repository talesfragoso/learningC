#include <stdio.h>

int main(int argc, char *argv[])
{

	int i,j;
	
	for(i = 1; i < argc; i++) 
		printf("%s\n", argv[i]);
	char *states[] = {"California", "Oregon", "Washington", "Texas"};
	int num_states = 4;
	for(i = 0; i < num_states; i++)
	{		
		for(j = 0; states[i][j] != '\0'; j++)
			printf("%c", states[i][j]);
	printf("\n");	
	}
	return 0;
}
